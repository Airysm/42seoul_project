int	ft_sqrt(int nb)
{
	int	num;

	if (nb <= 0)
		return (0);
	num = 1;
	while (num * num <= nb && num <= 46340)
	{
		if (nb == num * num)
			return (num);
		num++;
	}
	return (0);
}
