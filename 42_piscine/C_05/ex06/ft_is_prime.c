int	ft_is_prime(int nb)
{
	int	i;
	int	count;

	if (nb <= 1)
		return (0);
	i = 2;
	count = 0;
	while (i <= nb / i)
	{
		if (nb % i == 0)
			count++;
		if (count > 0)
			break ;
		i++;
	}
	if (count == 0)
		return (1);
	return (0);
}
