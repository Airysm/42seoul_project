#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	c %= 256;
	i = ft_strlen(s);
	while (s[i] != c)
	{
		if (i == 0)
			return (NULL);
		i--;
	}
	return ((char *)&s[i]);
}
